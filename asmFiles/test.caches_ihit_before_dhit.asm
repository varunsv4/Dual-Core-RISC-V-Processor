#----------------------------------------------------------
# RISC-V Assembly
#----------------------------------------------------------
#--------------------------------------
# IHIT BEFORE DHIT
#--------------------------------------
org 0x0000

li $5, 0        // loop variable
li $6, 2        // number of loops

li $7, 2616            // address 1
addi $28, $27, 512      // address 2

li $29, 32      // data 1
li $30, 64       // data 2

sw $29, 0($7)
sw $30, 0($28)

loop:
beq $5, $6, exit
addi $29, $29, 16
lw $31, 0($7)
addi $7, $7, 512
addi $5, $5, 1
j loop

exit:
halt
