# PIPELINE BRANCH NOT TAKEN
#--------------------------------------
org 0x0000

li $5, 7
li $6, 6
beq $5, $6, beq_taken       // Branch if equal
j end_test

beq_taken:

add $28, $5, $6             // $28 <--- 13
sw $28, 100($0)

end_test:
li $7, 9                    // $7 <--- 9
sub $29, $7, $5
sw $29, 100($0) 
halt
