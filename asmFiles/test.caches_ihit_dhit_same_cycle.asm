# RISC-V Assembly
#----------------------------------------------------------
#--------------------------------------
# IHIT AND DHIT SAME CYCLE
#--------------------------------------
org 0x0000

li $5, 0    // loop variable
li $6, 4    // number of loops

li $7, 0
li $28, 2616

loop:
beq $5, $6, exit
addi $7, $7, 8
sw $7, 0($28)
addi $5, $5, 1
j loop

exit:
halt
