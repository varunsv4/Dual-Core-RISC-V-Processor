#--------------------------------------
# PIPELINE BRANCH DEPENDENCY
#--------------------------------------
org 0x0000

li $5, 4
li $6, 5
li $7, 9

add $28, $5, $6             // $28 <--- 9
beq $28, $7, beq_taken       // Branch if equal

sub $29, $7, $5             // $29 <--- 5
sw $29, 100($0)
j end_test

beq_taken:

li $31, 1                   // #31 <--- 1
add $28, $7, $31            // $28 <--- 10
sw $28, 100($0)

end_test:
halt
