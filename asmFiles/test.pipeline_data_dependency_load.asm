#--------------------------------------
# PIPELINE DATA DEPENDENCY LOAD
#--------------------------------------
org 0x0000

li $5, 12
li $6, 23
li $7, 9

sw $6, 100($0)
add $28, $5, $7         // $28 <--- 21

lw $29, 100($0)         // $29 <--- 23
sub $30, $29, $28       // $30 <--- 2

sw $30, 100($0)

halt
